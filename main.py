a = int(input("Enter the first number (a): "))
b = int(input("Enter the second number (b): "))

#1. For floor division
floor_division = a // b
print("Floor Division (a // b):", floor_division)

#2. For a power b
power_result = a ** b
print("a power b (a ** b):", power_result)

#3. For Find out whether two variables have equal value or not - if yes then print True if they are not equal then print False

if a == b:
    print ("True")
else:
    print("False")